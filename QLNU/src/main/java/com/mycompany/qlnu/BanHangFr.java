	private void btnReSet(ActionEvent e) {
        int confirm = JOptionPane.showConfirmDialog(null, "Bạn có muốn xóa tất cả ?");
        if (confirm == JOptionPane.YES_OPTION) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            lbTongtienVND.setText("0");
            lbTienthuaVND.setText("0");
            txtTiennhan.setText("0");

        }

    }

    public void ResetTable() {

        Float sum = (float) 0;
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String textValue = (String) (jTable1.getValueAt(i, 3).toString().replaceAll("[ VNĐ]", "")).replaceAll(",", "");

            Float value = Float.parseFloat(textValue);
            sum = sum + value;
        }
        lbTongtienVND.setText(String.valueOf(sum));

    }

    private void btnFixnuocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFixnuocActionPerformed

        int select = jTable1.getSelectedRow();
        if (select != -1) {
            jTable1.setValueAt(cbbtenNuoc.getSelectedItem(), select, 0);
            jTable1.setValueAt(tfSoluong.getText(), select, 1);
            jTable1.setValueAt(lbGiaVND.getText(), select, 2);
            jTable1.setValueAt(lbThanhtienVND.getText(), select, 3);
            JOptionPane.showMessageDialog(null, "Đã cật nhập dữ liệu thành công");
        }
        ResetTable();
    }//GEN-LAST:event_btnFixnuocActionPerformed

