    public void tfSoluongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfSoluongActionPerformed

        String getTenNuoc = (String) cbbtenNuoc.getSelectedItem();
        float soLuong = Float.parseFloat(tfSoluong.getText());
        float giaBan = Float.parseFloat(String.valueOf(NuocUongDAO.getInstance().selectById(getTenNuoc).getGiaBan()));
        float thanhTien = soLuong * giaBan;

        if (getTenNuoc.equals(null)) {
            lbGiaVND.setText("0");
        } else {
            lbGiaVND.setText(String.valueOf(NuocUongDAO.getInstance().selectById(getTenNuoc).getGiaBan()));
            lbThanhtienVND.setText(String.valueOf(thanhTien));
        }
    }

